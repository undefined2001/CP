public class EscapeSequence {
    public static void main(String[] args){
        System.out.println(" Hello \n My name is Shakib");
        System.out.println(" My contact no is:\t01783920013");
        System.out.println(" \bBackspace");
        System.out.println(" Carriage \r Return");
        System.out.println(" \"Escape Sequence\"");
        System.out.println(" \'single quote\'");
        System.out.println("\\Backslash\\");
    }
}
/*
\b --------- backspace
\t --------- tab
\n --------- new line
\r --------- carriage return
\" --------- double quote
\' --------- single quote
\\ --------- backslash
 */